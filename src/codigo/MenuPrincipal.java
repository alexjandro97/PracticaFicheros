    private void copiarFicheros() {
        //declaramos las variables que contienen las rutas de los ficheros que vamos a usar
        String rutaOrigen = this.rutaOrigen.getText();
        String rutaDestino = this.rutaDestino.getText();
        //las rutas las pasamos como parametro para crear ficheros en java
        File origen = new File(rutaOrigen);
        File destino = new File(rutaDestino);
        //si el fichero existe, podemos copiarlo
        if (origen.exists() == true || destino.exists() == true) {
            InputStream fuente = null;
            byte[] buffer = new byte[1024];
            try (OutputStream destinoOK = new FileOutputStream(rutaDestino)) {
                fuente = new FileInputStream(rutaOrigen);
                int leemos;
                //leemos el documento origen 
                while ((leemos = fuente.read(buffer)) != -1) {
                    //escribimos en el documento destino
                    destinoOK.write(buffer, 0, leemos);
                }
                fuente.close();
                JOptionPane.showMessageDialog(null, "Fichero "+rutaOrigen+" copiado con exito.");
                copiarFicheroJDialog.setVisible(false);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "No se ha podido copiar el fichero.");
            }
        }
    }
    

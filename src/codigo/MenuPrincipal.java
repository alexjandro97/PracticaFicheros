    /*
    Metodo para crear un fichero o un directorio en la ruta indicada.
    */
    private void crearFicheroDirectorio() throws IOException {
        String ruta = rutaCrear.getText(); //guardamos la ruta del fichero/directorio
        /*
        COmo he creado un grupo de radio buttons, en funcion del radio que ha sido seleccionado
        hace una cosa u otra
        */
        
        //directorio, si el directorio con la ruta que le hemos pasado existe salta una alerta
        if (directorioRadio.isSelected()) {
            File directorio = new File(ruta);
            if (directorio.exists()) {
                JOptionPane.showMessageDialog(null, "La carpeta " + directorio.toString() + " ya existe con este nombre.");
            } else {
                //sino, creamos el directorio y hacemos saltar una alerta para saberlo.
                directorio.mkdir();
                JOptionPane.showMessageDialog(null, "La carpeta " + directorio.toString() + " ha sido creada.");
                crearCosasJDialog.setVisible(false);
            }
            //fichero - le pasamos la ruta y si el fichero existe salta la alerta
        } else if (ficheroRadio.isSelected()) {
            File archivo = new File(ruta);
            if (archivo.exists()) {
                JOptionPane.showMessageDialog(null, "El fichero " + archivo.toString() + " ya existe con este nombre.");
            } else {
                //sino creamos el fichero
                archivo.createNewFile();
                JOptionPane.showMessageDialog(null, "El fichero " + archivo.toString() + " ha sido creado.");
                crearCosasJDialog.setVisible(false);
            }
        }
    }
    /*
    metodo para la gestion de ficheros secuenciales, vamos a tener los metodos en funcion del 
    radio button que tengamos pulsado, escribir , sobreescribir, leer y buscar.
    COn interfaz grafica.
    */
    public void secuenciales() {
        String ruta = rutaSecuencialText.getText();
        String datos = datosNuevosText.getText();
        if (radioLeer.isSelected()) {
            String texto = new String();
            try {
                FileReader fr = new FileReader(ruta);
                //buffer de lectura enlazado al puente de comunicacion que es el FileReader
                //FIleReader es el conector entre el fichero y el buffered y este es una copia del archivo con la cual trabajamos
                BufferedReader entrada = new BufferedReader(fr);
                String s;

                // usamos el bucle para meter en en String s todas las lineas del fichero
                // mientras en la linea haya datos (que no sea null)
                while ((s = entrada.readLine()) != null) //texto = texto + s a parte
                {
                    texto += s + "\n";
                }
                verDatosImpresos.setVisible(true);
                datosLabel.setText("\nDatos leidos del archivo con READLINE: \n"
                        + texto);

                //System.out.print((char)entrada.read());
                //cerramos el buffer
                entrada.close();
            } catch (java.io.FileNotFoundException fnfEx) {
                System.out.println("Archivo no encontrado");
                JOptionPane.showMessageDialog(null, "No se ha podido encontrar su archivo o no existe.");
            } catch (java.io.IOException ioex) {
            }
        } else if (radioEscribir.isSelected()){
            try {
            //abro la relacion con el archivo que quiero escribir
            FileWriter fw = new FileWriter(ruta,true); //si pones true escribes detras y si pones false sobrescribes
            //creo una copia del archivo con el que voy a trabajar que apunta a la conexion
            BufferedWriter bw = new BufferedWriter(fw);
            //escribo los datos nuevos y cierro la conexion
            bw.write(datos + "\n");
            bw.close();
            JOptionPane.showMessageDialog(null, "Sus datos han sido escritos en el fichero "+ruta+".");
            datosNuevosText.setText("");
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "No se ha podido encontrar su archivo o no existe.");
        }
        } else if (radioSobre.isSelected()){
            try {
            //abro la relacion con el archivo que quiero escribir
            FileWriter fw = new FileWriter(ruta,false); //si pones true escribes detras y si pones false sobrescribes
            //creo una copia del archivo con el que voy a trabajar que apunta a la conexion
            BufferedWriter bw = new BufferedWriter(fw);
            //escribo los datos nuevos y cierro la conexion
            bw.write(datos + "\n");
            bw.close();
            JOptionPane.showMessageDialog(null, "Sus datos han sido sobreescritos en el fichero "+ruta+".");
            datosNuevosText.setText("");
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "No se ha podido encontrar su archivo o no existe.");
        }
        }else {
            String texto = new String();
        try {
            FileReader fr = new FileReader(ruta);
            BufferedReader entrada = new BufferedReader(fr);
            System.out.println("Conectada");
            String palabraABuscar = datosNuevosText.getText();
            String linea = "";
            palabraABuscar = datosNuevosText.getText();
            while ((linea = entrada.readLine()) != null) {
               texto += linea + "\n";
                if (linea.indexOf(palabraABuscar) != -1) {
                    int posChar = texto.indexOf(linea.substring(0,1));
                    verDatosImpresos.setVisible(true);
                    datosLabel.setText("Se encontro la palabra: \t" +linea + "\n en la posición " + posChar);
                }   
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "No se ha podido encontrar su archivo o no existe.");
        }
        }
    }
    
